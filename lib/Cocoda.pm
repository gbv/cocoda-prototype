package Cocoda;
#ABSTRACT: Manage cross-concordances between controlled vocabularies
use v5.14;
#VERSION

1;

=head1 DESCRIPTION

This Perl distribution contains Cocoda, a tool to manage (cross-)concordances
between controlled vocabularies (also known as mappings between terminologies).

=head1 OVERVIEW

=over

=item L<Cocoda::Server>

implements a web application to manage terminolgy mappings.

=item L<Cocoda::Terminology>

represents a selected terminology to be used in a Cocoda server.

=back

=cut

